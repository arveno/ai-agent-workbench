export function createCapabilityIntroConclusion(): string {
  return [
    '我可以作为一个教育数据分析助手，帮助你围绕教学质量数据做分析。',
    '',
    '目前我可以做这些事：',
    '',
    '1. 读取数据源结构，识别可分析的表和字段。',
    '2. 聚合教学相关数据，例如平均分、出勤率、作业完成率和异常指标。',
    '3. 根据工具结果生成图表数据，并在右侧展示执行步骤和工具结果。',
    '4. 基于真实数据生成简要分析结论，帮助你发现异常指标和后续关注方向。',
    '',
    '你可以这样问：',
    '',
    '- 分析本月教学质量数据，找出异常指标',
    '- 对比本月和上月的平均分变化',
    '- 看一下出勤率有没有明显下降',
    '- 找出异常数量较高的学科或班级',
  ].join('\n');
}

